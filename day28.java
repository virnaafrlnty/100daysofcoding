package pkg100dayss;

public class day28 {
    public static void main(String[] args) {
        // Array 1D dengan tipe data String
        String[] intArray = {"virna", "irma", "nurul", "randa", "fardan", "dhani"};
       

        // Mencetak elemen-elemen array
        System.out.println("Nama:");
        for (String num : intArray) {
            System.out.println(num + " ");
        }
    }
}

