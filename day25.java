package pkg100dayss;

public class day25 {
    public static void main(String[] args) {
        // Array 1D dengan tipe data int
        int[] intArray = {1, 2, 3, 4, 5};
       

        // Mencetak elemen-elemen array
        System.out.println("intArray:");
        for (int num : intArray) {
            System.out.print(num + " ");
        }
    }
}

